	public static final String SYS_KEY_PASS = "javax.net.ssl.keyPassword";
	public static final String SYS_KEYSTORE = "javax.net.ssl.keyStore";
	public static final String SYS_KEYSTORE_PASS = "javax.net.ssl.keyStorePassword";
	public static final String SYS_TRUSTSTORE = "javax.net.ssl.trustStore";
	public static final String SYS_TRUSTSTORE_PASS = "javax.net.ssl.trustStorePassword";

	private static final String PROTOCOL = "TLSv1.2";
	private static final String DEAFULT_KTPASS = "changeit";
	private static SSLContext taskMasterTLSContext = null;
	private static SSLContext taskAgentTLSContext = null;

	public FrameworkTLSContextFactory() {
	}

	public static synchronized SSLContext createTaskMasterTLSContext() {
		if (taskMasterTLSContext == null) {
			taskMasterTLSContext = createTLSContext(true);
		}

		return taskMasterTLSContext;
	}

	public static synchronized SSLContext createTaskAgentTLSContext() {
		if (taskAgentTLSContext == null) {
			taskAgentTLSContext = createTLSContext(false);
		}
		return taskAgentTLSContext;
	}

	protected static SSLContext createTLSContext(boolean master) {

		SSLContext tlsContext;
		try {
			String keypass = System.getProperty(KEYPASS);
			if (keypass == null) {
				keypass = System.getProperty(SYS_KEY_PASS);
			}
			if (keypass == null) {
				keypass = DEAFULT_KTPASS;
			}

			KeyStore ks = loadKeyStore(KEYSTORE, KEYSTORE_PASS, SYS_KEYSTORE, SYS_KEYSTORE_PASS, master);
			KeyManagerFactory kmf = KeyManagerFactory.getInstance(KeyManagerFactory.getDefaultAlgorithm());
			kmf.init(ks, keypass.toCharArray());
			KeyStore ts = loadKeyStore(TRUSTSTORE, TRUSTSTORE_PASS, SYS_TRUSTSTORE, SYS_TRUSTSTORE_PASS, !master);
			TrustManagerFactory tmf = TrustManagerFactory.getInstance(TrustManagerFactory.getDefaultAlgorithm());
			tmf.init(ts);
			tlsContext = SSLContext.getInstance(PROTOCOL);
			tlsContext.init(kmf.getKeyManagers(), tmf.getTrustManagers(), null);
		} catch (Exception e) {
			throw new RuntimeException("Failed to initialize the server-side SSLContext", e);
		}
		return tlsContext;
	}

	protected static KeyStore loadKeyStore(String keyProp, String kpProp, String sysKeyProp, String sysKpProp,
			boolean master) throws KeyStoreException, NoSuchAlgorithmException, CertificateException, IOException {

		KeyStore ks = KeyStore.getInstance(KeyStore.getDefaultType());
		String ksSysProp = System.getProperty(keyProp);
		String ksSysPass = System.getProperty(kpProp);
		if (ksSysProp == null) {
			ksSysProp = System.getProperty(sysKeyProp);
			ksSysPass = System.getProperty(sysKpProp);
		}
		if (ksSysPass == null) {
			ksSysPass = "";
		}
		if (ksSysProp != null) {
			FileInputStream fin = new FileInputStream(ksSysProp);
			try {
				ks.load(fin, ksSysPass.toCharArray());
			} finally {
				fin.close();
			}
		} else {
			if (master) {
				ks.load(new ByteArrayInputStream(TASKMASTER_CERTS), DEAFULT_KTPASS.toCharArray());
			} else {
				ks.load(new ByteArrayInputStream(TASKAGENT_CERTS), DEAFULT_KTPASS.toCharArray());
			}
		}
		return ks;
	}

