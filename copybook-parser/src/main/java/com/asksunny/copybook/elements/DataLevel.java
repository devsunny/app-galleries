package com.asksunny.copybook.elements;

public class DataLevel implements CopyBookElement{

	private String image;
	private int level;
	
	public DataLevel() {
		
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}
	
	
	

}
