package com.asksunny.spring.domain;

public class Flower {

	String name;
	String origin;
	String family;
	String category;
	String type;	
	int bloomingDuration;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getFamily() {
		return family;
	}
	public void setFamily(String family) {
		this.family = family;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getBloomingDuration() {
		return bloomingDuration;
	}
	public void setBloomingDuration(int bloomingDuration) {
		this.bloomingDuration = bloomingDuration;
	}
	
	
	

}
