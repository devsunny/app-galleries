package com.asksunny.odbc;

public class SQLProxyException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SQLProxyException() {
		super();
		
	}

	public SQLProxyException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public SQLProxyException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public SQLProxyException(String message) {
		super(message);
		
	}

	public SQLProxyException(Throwable cause) {
		super(cause);
		
	}
	
	

}
